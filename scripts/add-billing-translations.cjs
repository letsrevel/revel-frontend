/**
 * Deep-set a value at a dot-separated path in an object.
 * Creates intermediate objects as needed.
 */
function deepSet(obj, dotPath, value) {
	const parts = dotPath.split('.');
	let current = obj;
	for (let i = 0; i < parts.length - 1; i++) {
		if (!(parts[i] in current) || typeof current[parts[i]] !== 'object') {
			current[parts[i]] = {};
		}
		current = current[parts[i]];
	}
	const lastKey = parts[parts.length - 1];
	if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
		if (typeof current[lastKey] !== 'object' || current[lastKey] === null) {
			current[lastKey] = {};
		}
		Object.assign(current[lastKey], value);
	} else {
		current[lastKey] = value;
	}
}

function addTranslations(lang, newTranslations) {
	const filePath = path.join(__dirname, '..', 'messages', `${lang}.json`);
	const content = fs.readFileSync(filePath, 'utf8');
	const data = JSON.parse(content);

	for (const [key, value] of Object.entries(newTranslations)) {
		deepSet(data, key, value);
	}

	fs.writeFileSync(filePath, JSON.stringify(data, null, '\t'));
	console.log(`Added billing translations to ${lang}.json`);
}

Object.keys(translations).forEach((lang) => {
	addTranslations(lang, translations[lang]);
});

