import { writeFileSync, readFileSync, existsSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';
import { createClient } from '@hey-api/openapi-ts';

// Try local backend artifact first, fall back to live server
const __dirname = dirname(fileURLToPath(import.meta.url));
const LOCAL_SPEC = resolve(__dirname, '../../revel-backend/.artifacts/openapi.json');
const BACKEND_URL = process.env.BACKEND_URL || 'http://localhost:8000';
const OPENAPI_PATH = existsSync(LOCAL_SPEC) ? LOCAL_SPEC : `${BACKEND_URL}/api/openapi.json`;

console.log(`Generating API client from ${OPENAPI_PATH}...`);

try {
	await createClient({
		client: '@hey-api/client-fetch',
		input: OPENAPI_PATH,
		output: {
			path: './src/lib/api/generated',
			// No `lint` post-processor: the generated dir is eslint-ignored, and
			// ESLint 10 exits non-zero when every file in the glob is ignored.
			format: 'prettier'
		},
		types: {
			enums: 'typescript'
		},
		schemas: false,
		services: {
			asClass: false
		}
	});

	console.log('✅ API client generated!');
	console.log('📝 Removing hash suffixes from function names...');

	// Remove hash suffixes from generated SDK
	const sdkPath = './src/lib/api/generated/sdk.gen.ts';
	let sdkContent = readFileSync(sdkPath, 'utf-8');

	// Replace function names with hash suffixes (e.g., accountMe08B35537 -> accountMe)
	// Pattern: export const functionName[Hash] = -> export const functionName =
	sdkContent = sdkContent.replace(/export const ([a-zA-Z]+)[0-9A-Fa-f]{8,}/g, 'export const $1');

	// Also replace in function calls within the file
	sdkContent = sdkContent.replace(/\b([a-zA-Z]+)[0-9A-Fa-f]{8,}\(/g, '$1(');

	writeFileSync(sdkPath, sdkContent);
	console.log('✅ Hash suffixes removed from SDK!');

	// hey-api ≥0.99 emits generated/index.ts as an explicit named-export list that
	// still uses the hashed function names. Replace it with a barrel so the
	// de-hashed sdk.gen.ts names are what $lib/api re-exports.
	const generatedIndexPath = './src/lib/api/generated/index.ts';
	writeFileSync(
		generatedIndexPath,
		`// This file is auto-generated by @hey-api/openapi-ts

export type * from './types.gen';
export * from './sdk.gen';
`
	);
	console.log('✅ Generated index rewritten as barrel!');

	// Point the client's baseUrl at the centralized, RUNTIME-resolved API URL.
	// API_BASE_URL reads $env/dynamic/public, so a single prebuilt image can be
	// pointed at any backend by setting PUBLIC_API_URL at container runtime (#396).
	console.log('📝 Configuring API client to use API_BASE_URL (runtime)...');
	const clientPath = './src/lib/api/generated/client.gen.ts';
	let clientContent = readFileSync(clientPath, 'utf-8');

	// Add the centralized API config import at the top
	if (!clientContent.includes("from '$lib/config/api'")) {
		clientContent = clientContent.replace(
			/(\/\/ This file is auto-generated[^\n]*\n\n)/,
			"$1import { API_BASE_URL } from '$lib/config/api';\n"
		);
	}

	// Replace hardcoded baseUrl with the centralized runtime value
	// Original: baseUrl: 'http://localhost:8000'
	// New: baseUrl: API_BASE_URL
	// Fail fast if hey-api's output format drifts and the rewrite is a no-op,
	// rather than emitting a client that silently keeps the hardcoded URL.
	const rewritten = clientContent.replace(/baseUrl:\s*['"].*?['"]/, 'baseUrl: API_BASE_URL');
	if (rewritten === clientContent) {
		throw new Error(
			'Failed to rewrite generated client baseUrl to API_BASE_URL — the expected ' +
				"`baseUrl: '...'` literal was not found in client.gen.ts."
		);
	}
	clientContent = rewritten;

	writeFileSync(clientPath, clientContent);
	console.log('✅ API client configured to use API_BASE_URL (runtime)!');

	// Create index.ts to export everything
	const indexContent = `// Re-export everything from generated API
export * from './generated';
export { client } from './generated/client.gen';
`;

	writeFileSync('./src/lib/api/index.ts', indexContent);

	console.log('✅ API client generated successfully!');
	console.log('   Generated files:');
	console.log('   - src/lib/api/generated/types.gen.ts');
	console.log('   - src/lib/api/generated/sdk.gen.ts');
	console.log('   - src/lib/api/generated/client.gen.ts');
	console.log('   - src/lib/api/generated/index.ts');
	console.log('   - src/lib/api/index.ts');
} catch (error) {
	console.error('❌ Failed to generate API client:');
	console.error(error.message);
	process.exit(1);
}
